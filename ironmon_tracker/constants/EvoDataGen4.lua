function EvoDataGen4.parse()
    local completeString = ""
    local currentIndex = 1
    local lines = MiscUtils.readLinesFromFile("evorecords.json")
    local currentBase = ""
    local currentBaseTarget = ""
    local currentString = ""
    while currentIndex <= #lines do
        if lines[currentIndex]:find("{") then
            local gen = lines[currentIndex + 1]
            if (tonumber(gen:match("%d+")) == 5) then
                local baseName = lines[currentIndex + 2]
                local baseTargetName = lines[currentIndex + 4]
                local baseTargetIndex = lines[currentIndex + 5]
                if baseName ~= currentBase then
                    currentString = currentString .. "}},\n"
                    completeString = completeString .. currentString
                    currentBase = baseName
                    local baseIndexStr = lines[currentIndex + 3]
                    local baseIndex = baseIndexStr:match("%d+")
                    currentString = "[" .. baseIndex .. "] = {"
                    currentBaseTarget = ""
                end
                if baseTargetName ~= currentBaseTarget then
                    if currentBaseTarget ~= "" then
                        currentString = currentString .. "},\n"
                    end
                    currentString = currentString .. "[" .. baseTargetIndex:match("%d+") .. "] = {"
                    currentBaseTarget = baseTargetName
                end
                local targetIndexStr = lines[currentIndex + 7]
                local targetPercentStr = lines[currentIndex + 8]
                local targetIndex = tonumber(targetIndexStr:match("%d+"))
                local targetPercent = tonumber(targetPercentStr:match("%d+%.%d%d%d"))
                targetPercent = math.floor(targetPercent * 100 + 0.5) / 100
                local roundedPercent = string.format("%.2f", targetPercent)
                if tonumber(roundedPercent) >= 1.00 then
                    currentString = currentString .. "{"
                    currentString = currentString .. "id = " .. targetIndex .. " , "
                    currentString = currentString .. "percent = " .. roundedPercent
                    currentString = currentString .. "},"
                end
            end
        end
        currentIndex = currentIndex + 1
    end
    currentString = currentString .. "}},\n"
    completeString = completeString .. currentString
    MiscUtils.writeStringToFile("luaevodata.txt", completeString)
    print("done")
end
